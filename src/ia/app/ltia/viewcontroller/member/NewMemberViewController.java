    private void jTextFieldPasswordKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldPasswordKeyReleased
        String checkPassword = jTextFieldPassword.getText();

        if(LifeTrackerHelper.checkPassword(checkPassword)){
            isValidPassword = true;
            validPassword.setIcon(new ImageIcon(getClass().getResource(LifeTrackerConstants.TICK_ICON)));
        }
        else{
            isValidPassword = false;
            if(jTextFieldPassword.getText().trim().isEmpty()){
                validPassword.setIcon(new ImageIcon(getClass().getResource(LifeTrackerConstants.EMPTY_ICON)));
            }
            else{
                validPassword.setIcon(new ImageIcon(getClass().getResource(LifeTrackerConstants.CROSS_ICON)));
            }
        }
    }//GEN-LAST:event_jTextFieldPasswordKeyReleased

