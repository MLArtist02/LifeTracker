    private void SUBMITNEWMEMBERActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SUBMITNEWMEMBERActionPerformed
        // TODO add your handling code here:

        name = jTextFieldName.getText();
        password = jTextFieldPassword.getText();
        email = jTextFieldEmail.getText();
        if(LifeTrackerHelper.checkPassword(password) && password.length()>=8 && LifeTrackerHelper.checkName(name) && LifeTrackerHelper.checkEmail(email)){

            // At this point, we are good to create a new Member inside Database class will be called to
            // insert the new member details into the database

            //we must hash the password first because we don't want to save it as raw password
            hashedPassword = BCrypt.hashpw(password, BCrypt.gensalt());
            Member newMember = new Member(name, hashedPassword, email, true);

            //Go to the next screen but ...
            NewMemberDetailsViewController form2 = new NewMemberDetailsViewController();

            //... make it aware of the new member by calling it's setMember method
            form2.setMember(newMember);
            form2.setVisible(true);
            this.dispose();
        }
        else {
            if(LifeTrackerHelper.checkPassword(password) && password.length()>=8 && LifeTrackerHelper.checkName(name)){
                INFOMEMBER.setText("Invalid Email, Please Try Again");
                //set relative text underlined red to signify whcih aspects are needed correction (and below)
            }
            else if(LifeTrackerHelper.checkPassword(password) && password.length()>=8){
                INFOMEMBER.setText("Invalid Name & Email, Please Try Again");
            }
            else if(LifeTrackerHelper.checkName(name) && LifeTrackerHelper.checkEmail(email)){
                INFOMEMBER.setText("Invalid Password, Please Try Again");
            }
            else if(LifeTrackerHelper.checkName(name)){
                INFOMEMBER.setText("Invalid Email & Password, Please Try Again");
            }
            else if(LifeTrackerHelper.checkEmail(email)){
                INFOMEMBER.setText("Invalid Name & Password, Please Try Again");
            }
            else {
                INFOMEMBER.setText("All Fields Are Invalid, Please Try Again");
            }
        }

    }//GEN-LAST:event_SUBMITNEWMEMBERActionPerformed

