    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String exerciseName = jTextFieldExerciseName.getText();
        String caloriesBurned = jTextFieldCaloriesBurned.getText();
        String durationInMinutes = jTextFieldDurationMinutes.getText();
        
        if( LifeTrackerHelper.isValidString(exerciseName) &&
            LifeTrackerHelper.isValidDouble(caloriesBurned) && 
            LifeTrackerHelper.isValidDouble(durationInMinutes) ) {
            
            //if we are inside here, it means we have valid data and can save the exercise
            Database db = new Database(LifeTrackerConstants.DATABASE_NAME);
            db.addNewExercise(exerciseName, Double.parseDouble(caloriesBurned), Double.parseDouble(durationInMinutes));
                    
            //Because we saved a new exercise, we need to re-load the exercises in the parent window
            parentMustReloadExercises = true;            
            
            //Empty the JTextFields
            jTextFieldExerciseName.setText("");
            jTextFieldCaloriesBurned.setText("");
            jTextFieldDurationMinutes.setText("");
            
            //And set the status to show the exercise was added            
            jLabelStatus.setText("New exercise " + exerciseName + " added!");
            jLabelStatus.setForeground(Color.BLACK);
            jLabelStatus.setIcon(new ImageIcon(getClass().getResource(LifeTrackerConstants.TICK_ICON)));
        }
        else {
            //invalid data found
            jLabelStatus.setText("Invalid data. Please correct and re-try.");
            jLabelStatus.setForeground(Color.RED);
            jLabelStatus.setIcon(new ImageIcon(getClass().getResource(LifeTrackerConstants.CROSS_ICON)));
            
        }   
    }//GEN-LAST:event_jButton1ActionPerformed

