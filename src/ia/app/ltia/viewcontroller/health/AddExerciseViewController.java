    private void jTextFieldCaloriesBurnedKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldCaloriesBurnedKeyReleased
        String checkInt = jTextFieldCaloriesBurned.getText(); 
        
        jLabelStatus.setText("");
        jLabelStatus.setIcon(new ImageIcon(getClass().getResource(LifeTrackerConstants.EMPTY_ICON)));

        if(LifeTrackerHelper.checkNumber(checkInt)){
            isValidCalories = true;
            validCalories.setIcon(new ImageIcon(getClass().getResource(LifeTrackerConstants.TICK_ICON)));
        }
        else{
            isValidCalories = false;
            if(jTextFieldCaloriesBurned.getText().trim().isEmpty()){
                validCalories.setIcon(new ImageIcon(getClass().getResource(LifeTrackerConstants.EMPTY_ICON)));
            }
            else{
                validCalories.setIcon(new ImageIcon(getClass().getResource(LifeTrackerConstants.CROSS_ICON)));
            }
        }                 
    }//GEN-LAST:event_jTextFieldCaloriesBurnedKeyReleased

