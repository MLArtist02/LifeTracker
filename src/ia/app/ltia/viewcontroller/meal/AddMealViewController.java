    private void jButtonSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSaveActionPerformed
        
        String mealDescription = jTextFieldMealDescription.getText();
        String proteinAsString = jTextFieldProtein.getText();
        String carbsAsString = jTextFieldCarbs.getText();
        String fiberAsString = jTextFieldFiber.getText();
        String fatAsString = jTextFieldFat.getText();
        String caloriesAsString = jTextFieldCalories.getText();
        String servingSizeAsString = jTextFieldServingSize.getText();
        
        if( LifeTrackerHelper.isValidString(mealDescription) &&
            LifeTrackerHelper.isValidDouble(proteinAsString) &&
            LifeTrackerHelper.isValidDouble(carbsAsString) &&
            LifeTrackerHelper.isValidDouble(fiberAsString) &&
            LifeTrackerHelper.isValidDouble(fatAsString) &&
            LifeTrackerHelper.isValidDouble(caloriesAsString) &&
            LifeTrackerHelper.isValidDouble(servingSizeAsString)) {

            String servingSizeUnit = jComboBoxServingSizeUnit.getSelectedItem().toString();
            
            Database db = new Database(LifeTrackerConstants.DATABASE_NAME);
            if(db.addNewMeal(mealDescription, Double.parseDouble(proteinAsString), Double.parseDouble(carbsAsString),
                    Double.parseDouble(fiberAsString), Double.parseDouble(fatAsString), Double.parseDouble(caloriesAsString),
                    Double.parseDouble(servingSizeAsString), servingSizeUnit)){
                
                //Because we saved a new meal, we need to re-load the meals in the parent window
                parentMustReloadMeals = true;     
                
                jLabelStatus.setText("Meal was saved!");
                jLabelStatus.setForeground(Color.BLACK);
                jLabelStatus.setIcon(new ImageIcon(getClass().getResource(LifeTrackerConstants.TICK_ICON)));
                
            }
            else {
                jLabelStatus.setText(LifeTrackerConstants.DATABASE_ERROR_MESSAGE);
                jLabelStatus.setForeground(Color.RED);
                jLabelStatus.setIcon(new ImageIcon(getClass().getResource(LifeTrackerConstants.CROSS_ICON)));
            }
        }                 
    }//GEN-LAST:event_jButtonSaveActionPerformed

