    private void jTextFieldProteinKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldProteinKeyReleased
        String checkInt = jTextFieldProtein.getText();
        
        jLabelStatus.setText("");
        jLabelStatus.setIcon(new ImageIcon(getClass().getResource(LifeTrackerConstants.EMPTY_ICON)));             

        if(LifeTrackerHelper.checkNumber(checkInt)){
            isValidProtein = true;
            validProtein.setIcon(new ImageIcon(getClass().getResource(LifeTrackerConstants.TICK_ICON)));
        }
        else{
            isValidProtein = false;
            if(jTextFieldProtein.getText().trim().isEmpty()){
                validProtein.setIcon(new ImageIcon(getClass().getResource(LifeTrackerConstants.EMPTY_ICON)));
            }
            else{
                validProtein.setIcon(new ImageIcon(getClass().getResource(LifeTrackerConstants.CROSS_ICON)));
            }
        }
    }//GEN-LAST:event_jTextFieldProteinKeyReleased

